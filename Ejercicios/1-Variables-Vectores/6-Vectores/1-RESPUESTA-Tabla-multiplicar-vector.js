// Definimos el número multiplicador
const multiplicador = 5;

// Definimos el vector con los multiplicadores
var multiplicadores = [
    1,
    2,
    3,
    4,
];

// Los resultados tienen que estar vacíos
var resultados = [];
resultados.push(multiplicador * multiplicadores[0]);
resultados.push(multiplicador * multiplicadores[1]);
resultados.push(multiplicador * multiplicadores[2]);
resultados.push(multiplicador * multiplicadores[3]);

// Mostramos los resultados
console.log(resultados);
